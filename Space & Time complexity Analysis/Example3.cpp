#include<iostream>
#include<vector>
#include<algorithm>
 using namespace std;

 void Bubble_Sort(int arr[], int n){

    int i,j;
     bool swapped;
     for(int i=0; i<n-1; i++){
        swapped false;
        for(int j=0; j<n-1; j++) {
            swap(&arr[j], arr[j+1]);
            swapped true;


        }
     }

   if(swapped= false);
      break;     
    
 }